package dto

import "src/internal/models"

type TrackMeta struct {
	Id    uint64  `json:"id"`
	Name  string  `json:"name"`
	Genre *string `json:"genre"`
}

type TrackMetaWithoutId struct {
	Name  string  `json:"name"`
	Genre *string `json:"genre"`
}

type TrackObjectWithoutId struct {
	TrackMetaWithoutId
	Payload     []byte `json:"payload"`
	PayloadSize int64  `json:"payload_size"`
}

type TrackObject struct {
	TrackMeta
	Payload     []byte `json:"payload"`
	PayloadSize int64  `json:"payload_size"` // TODO: mb удалить PayloadSize
}

type TrackObjectWithSource struct {
	TrackMeta
	Source      string `json:"source"`
	Payload     []byte `json:"payload"`
	PayloadSize int64  `json:"payload_size"` // TODO: mb удалить PayloadSize
}

type TracksMetaCollection struct {
	Tracks []*TrackMeta `json:"tracks"`
}

func ToDtoTrackMeta(m *models.TrackMeta) *TrackMeta {
	var genre *string
	if m.Genre == "" {
		genre = nil
	} else {
		genre = &m.Genre
	}

	return &TrackMeta{
		Id:    m.Id,
		Name:  m.Name,
		Genre: genre,
	}
}

func ToModelTrackObjectWithoutId(t *TrackObjectWithoutId, id uint64, source string) *models.TrackObject {
	var genre string
	if t.Genre == nil {
		genre = ""
	} else {
		genre = *t.Genre
	}
	return &models.TrackObject{
		TrackMeta: models.TrackMeta{
			Id:     id,
			Source: source,
			Name:   t.Name,
			Genre:  genre,
		},
		Payload:     t.Payload,
		PayloadSize: t.PayloadSize,
	}
}

func ToDtoTrackObjectWithSource(t *models.TrackObject) *TrackObjectWithSource {
	var genre *string
	if t.Genre == "" {
		genre = nil
	} else {
		genre = &t.Genre
	}
	return &TrackObjectWithSource{
		TrackMeta: TrackMeta{
			Id:    t.Id,
			Name:  t.Name,
			Genre: genre,
		},
		Source:      t.Source,
		Payload:     t.Payload,
		PayloadSize: t.PayloadSize,
	}
}
